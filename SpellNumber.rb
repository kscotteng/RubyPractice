# this program will take a number from 0 - 100 and spell it out

def spellNumber (number)
  if number < 0
    puts 'Please enter a number greater than 0'
  elsif number > 100
    puts 'Please enter a number less than 100'
  elsif
    puts number
  end
end

spellNumber(-5)
spellNumber(110)
spellNumber(54)
